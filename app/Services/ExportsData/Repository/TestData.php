    /**
     * Logo data
     * @var string
     */
    protected static $logo = 'https://collierstech.ru/funcs/logo.png';

    /**
     * Get data by feed id
     * @param  int    $id
     * @return array
     */
    public static function getData(int $id)
    {
        if (! $data = (static::$data[$id] ?? [])) {
            return [];
        }

        $itemsArr = [];

        foreach ($data as $item) {
            $itemObj = (object) $item;
            $itemsArr[] = $itemObj;
        }

        $collection = collect($itemsArr);
        $dataChunks = $collection->chunk(5);

        return $dataChunks->all();
    }

    /**
     * Get the logo url
     * @return [type] [description]
     */
    public static function getLogo()
    {
        return static::$logo;
    }

